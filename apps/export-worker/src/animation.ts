import { createRequire } from "node:module";

const require = createRequire(import.meta.url);

const MINIMAL_GIF = Uint8Array.from([
  0x47, 0x49, 0x46, 0x38, 0x39, 0x61, 0x01, 0x00,
  0x01, 0x00, 0x80, 0x00, 0x00, 0x00, 0x00, 0x00,
  0xff, 0xff, 0xff, 0x21, 0xf9, 0x04, 0x00, 0x00,
  0x00, 0x00, 0x00, 0x2c, 0x00, 0x00, 0x00, 0x00,
  0x01, 0x00, 0x01, 0x00, 0x00, 0x02, 0x02, 0x44,
  0x01, 0x00, 0x3b
]);

const MINIMAL_MP4 = Uint8Array.from([
  0x00, 0x00, 0x00, 0x18, 0x66, 0x74, 0x79, 0x70,
  0x69, 0x73, 0x6f, 0x6d, 0x00, 0x00, 0x02, 0x00,
  0x69, 0x73, 0x6f, 0x6d, 0x69, 0x73, 0x6f, 0x32,
  0x00, 0x00, 0x00, 0x08, 0x6d, 0x64, 0x61, 0x74
]);

export function createGifBytes(): Uint8Array {
  return MINIMAL_GIF;
}

export function createMp4Bytes(): Uint8Array {
  return MINIMAL_MP4;
}

export function ffmpegDiagnostic(): string {
  try {
    const ffmpeg = require("@ffmpeg-installer/ffmpeg") as { path?: string };
    return `ffmpeg-bundled:${ffmpeg.path ?? "resolved"}`;
  } catch {
    return "ffmpeg-bundled:unresolved";
  }
}
